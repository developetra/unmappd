package com.example.unmappd.backend;

public class Landmark {
    private String name;
    private int longitude;
    private int latitude;

    // Default Constructor
    public Landmark() {
        super();
    }

    // Constructor
    public Landmark(String name, int longitude, int latitude) {
        this.name = name;
        this. longitude = longitude;
        this.latitude = latitude;
    }

}
